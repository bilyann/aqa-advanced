const name1 = "Anna";
const name2 = "Dima";

// 1. Використання конкатенації 
const greetingConcatenation = "Hello, " + name1 + " and " + name2 + "! Welcome back to class.";
console.log("Concatenation result:", greetingConcatenation);

// 2. Використання шаблонного рядка (Template Literals)
const greetingTemplateLiteral = `Hello, ${name1} and ${name2}! Welcome back to class.`;
console.log("Template literal result:", greetingTemplateLiteral);