// 1. Створюємо рекурсивну функцію-лічильник
function countDown(num) {
    // 2. Виводимо поточне значення в консоль
    console.log(num);

    // 3. Базовий випадок (умова зупинки рекурсії)
    if (num <= 1) {
        return;
    }

    // 4. Рекурсивний виклик зі зменшеним значенням
    countDown(num - 1);
}

// 5. Виклик функції для перевірки
countDown(5);