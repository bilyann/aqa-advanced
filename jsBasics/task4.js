// 4.1 Площа кола
let radiusCircle = 7;
let circleArea = Math.PI * Math.pow(radiusCircle, 2);
console.log("Площа кола:", circleArea.toFixed(2));

// 4.2 Площа прямокутника
let length = 10;
let width = 5;
let rectangleArea = length * width;
console.log("Площа прямокутника:", rectangleArea.toFixed(2));

// 4.3 Об'єм циліндра
let radiusCylinder = 3;
let heightCylinder = 7;
let cylinderVolume = Math.PI * Math.pow(radiusCylinder, 2) * heightCylinder;
console.log("Об'єм циліндра:", cylinderVolume.toFixed(2));